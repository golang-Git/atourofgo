package main

import (
	"fmt"
)

/*
🤔もんだい 3: 下記の三角形 (19x10)を出力してみよう

         *
        ***
       *****
      *******
     *********
    ***********
   *************
  ***************
 *****************
*******************

*/

func main() {
	// 😤
	for i := 1; i < 10; i++ {

		for j := 1; j+10 > i; j-- { //条件式で計算を行うと負荷がかかるので変数として定義する
			fmt.Print(" ")
		}

		for j := 1; j < i*2; j++ {
			fmt.Print("*")
		}
		fmt.Println("")
	}

}
